# esses são todos os operadores que podem ser usados para números

print(5+2) # --> 7
print(5-2) # --> 3
print(5/2) # --> 2.5 (a divisão usando barra única sempre retorna "float"
print(5//2) # --> mostra o piso da divisão --> 2
print(5%2) # pega o resto da divisão --> 1
print(5*2) # --> 10
print(5**2) #exponenciação --> 25
print(-3**2) # --> -9
print((-3)**2) # --> 9

# operadores com operandos de diferentes tipos convertem o inteiro para ponto flutuante:
print(4 * 3.75 - 1) # --> 14.0
